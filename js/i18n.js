const KIND_KEYS = {
  Wallet: "kindWallet",
  "Liquidity Pool": "kindLiquidityPool",
  Lending: "kindLending",
  Yield: "kindYield",
  Farming: "kindFarming",
  Deposit: "kindDeposit",
};

export function translateKind(lang, kind) {
  const key = KIND_KEYS[kind];
  if (key) return dict[lang]?.[key] ?? dict.en[key] ?? kind;
  return kind;
}

export function t(lang, key, vars) {
  let s = dict[lang]?.[key] ?? dict.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(`{${k}}`, String(v));
    }
  }
  return s;
}
